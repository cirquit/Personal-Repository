-- vim cheatsheet

-- Jumping words

  * w / W   -- (Word)
  * e / E   -- (End)

  * b / B            -- (Back)
  * g + e / g + E 

  * $   -- (End)
  * 0   -- (Pos1)

  * [num]f + <char>      -- (jump forward to num * char)
     ** ;           -- (repeat)
     ** F + <char>  -- (backwards)


  * [num]t + <char>      -- (jump forward till num * char)
     ** ;           -- (repeat)
     ** T <char>    -- (backwards)


